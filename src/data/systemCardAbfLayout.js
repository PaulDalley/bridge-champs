export function getVisibleSectionById(sectionId) {
  return VISIBLE_CARD_SECTIONS.find((s) => s.id === sectionId);
}

export function getVisibleFieldValue(abfValues, visibleField) {
  const first = (visibleField.pdfFields || [])
    .map((f) => String(abfValues[f] || "").trim())
    .find(Boolean);
  return first || "";
}

export function setVisibleFieldValue(abfValues, visibleField, value) {
  const text = String(value || "");
  const next = { ...abfValues };
  (visibleField.pdfFields || []).forEach((f) => {
    next[f] = text;
  });
  return next;
}