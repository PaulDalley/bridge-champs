const FINAL_TAKEAWAY_MARKER = "<h2>Final takeaway</h2>";
const PART2_HEADING_MARKER = "<h2>Part 2 —";

/** Place outline before Part 2 (or legacy Final takeaway if still present). */
function insertOutlineBeforePart2(html, outline) {
  const markers = [PART2_HEADING_MARKER, FINAL_TAKEAWAY_MARKER];
  let idx = -1;
  for (const marker of markers) {
    const at = html.indexOf(marker);
    if (at !== -1 && (idx === -1 || at < idx)) idx = at;
  }
  if (idx === -1) {
    return `${html.trim()}\n\n${outline}`;
  }
  return `${html.slice(0, idx).trimEnd()}\n\n${outline}\n\n${html.slice(idx)}`;
}

function initFirebase() {
  const keyPathCandidates = [
    path.join(__dirname, "..", "serviceAccountKey.json"),
    path.join(os.homedir(), "Downloads", "bridgechampions-firebase-adminsdk-fbsvc-a2157e530a.json"),
  ];
  const keyPath = keyPathCandidates.find((p) => fs.existsSync(p));
  if (!keyPath) {
    throw new Error("No Firebase service account key found.");
  }
  admin.initializeApp({
    credential: admin.credential.cert(JSON.parse(fs.readFileSync(keyPath, "utf8"))),
  });
  return admin.firestore();
}

function stripReadNextBackfill(html) {
  return String(html)
    .replace(/<!--\s*BACKFILL:read-next:v1\s*-->[\s\S]*?<!--\s*\/BACKFILL:read-next:v1\s*-->/g, "")
    .trim();
}

